"""
Subumbra Adapter #1 — LiteLLM
─────────────────────────────────────────────────
This file implements the LiteLLM adapter for Subumbra. LiteLLM now reaches the
Subumbra core through the canonical POST /proxy API using a custom transport.

Canonical Subumbra core API: POST /proxy — see docs/adapter-contract.md

The callback fetches subumbra records and injects V2 envelope metadata. The
transport owns /proxy packaging and sends the fully assembled upstream request
to the Worker. The old header-gated compatibility route is gone.

─────────────────────────────────────────────────
Intercepts any LiteLLM call whose api_key starts with "subumbra:" and:

  1. Extracts the key_id (e.g. "subumbra:anthropic_prod" → "anthropic_prod")
  2. Fetches the AES-256-GCM ciphertext from subumbra-keys  (internal network)
  3. Injects the Subumbra envelope metadata needed by the transport
  4. Logs success/failure for the UI dashboard

How routing works now:
  LiteLLM builds its native provider request URL first.

  The callback injects:
    X-Subumbra-Provider     → required mismatch-protection identity
    X-Subumbra-Ciphertext   → encrypted API key envelope
    X-Subumbra-Wrapped-Dek  → RSA-wrapped per-record DEK
    X-Subumbra-Pub-Key-Fp   → wrapping key fingerprint
    X-Subumbra-Key-Id       → AAD-bound record identity

  SubumbraTransport intercepts the outbound request, strips internal headers,
  packages the request into the canonical /proxy JSON envelope, and sends it to
  the Worker. The Worker validates, decrypts, injects auth, and proxies to the
  real upstream.

Hook used: async_pre_call_deployment_hook
  This fires AFTER the router selects a deployment and api_key from litellm_params
  is available in kwargs. async_pre_call_hook fires too early (proxy request level)
  before the model's api_key is resolved from config.

Required environment variables (set in docker-compose.yml, not secrets):
  SUBUMBRA_KEYS_URL      http://subumbra-keys:9090
  SUBUMBRA_ACCESS_TOKEN  hex token (generated by bootstrap)
  SUBUMBRA_HMAC_KEY      hex key   (generated by bootstrap)
  CF_WORKER_URL          https://subumbra-proxy.workers.dev

Optional:
  CF_ACCESS_CLIENT_ID       CF Access service token (if Worker is behind CF Access)
  CF_ACCESS_CLIENT_SECRET
"""


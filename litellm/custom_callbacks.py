# Module-level instance — referenced by litellm_settings.callbacks in config.yaml
# LiteLLM imports this module and reads this name directly; the config value
# must be "custom_callbacks.proxy_handler_instance" (instance, not class).
# ─────────────────────────────────────────────────────────────────────────────
proxy_handler_instance = ForgeKeyCallback()

# ── Self-register with LiteLLM (intentionally redundant) ───────────────
# LiteLLM's config resolution via initialize_callbacks_on_proxy() will also
# register this instance. This import-time append is belt-and-suspenders:
# it guarantees the hook is active even if LiteLLM's config processing
# changes in a future version. The try/except catches a potential circular
# import during early initialization — in that case, config-based
# registration is the primary path.
try:
    import litellm as _litellm
    if proxy_handler_instance not in _litellm.callbacks:
        _litellm.callbacks.append(proxy_handler_instance)
except Exception:
    pass  # Will be registered via config instead

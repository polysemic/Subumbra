import hashlib
import hmac
import json
import logging
import os
import secrets
import time
from typing import Optional

import httpx
from litellm.integrations.custom_logger import CustomLogger

# ─────────────────────────────────────────────────────────────────────────────
# Configuration  (read once at module load)
# ─────────────────────────────────────────────────────────────────────────────

SUBUMBRA_KEYS_URL       = os.environ.get("SUBUMBRA_KEYS_URL",       "http://subumbra-keys:9090").rstrip("/")
SUBUMBRA_ACCESS_TOKEN   = os.environ.get("SUBUMBRA_ACCESS_TOKEN",   "")
SUBUMBRA_HMAC_KEY       = os.environ.get("SUBUMBRA_HMAC_KEY",       "").encode()
CF_WORKER_URL         = os.environ.get("CF_WORKER_URL",         "").rstrip("/")
CF_ACCESS_CLIENT_ID   = os.environ.get("CF_ACCESS_CLIENT_ID",   "")
CF_ACCESS_CLIENT_SECRET = os.environ.get("CF_ACCESS_CLIENT_SECRET", "")

# ─────────────────────────────────────────────────────────────────────────────
# Logging
# ─────────────────────────────────────────────────────────────────────────────

log = logging.getLogger("subumbra-callback")

TRANSPORT_STRIP_HEADERS = {
    "cf-cache-status",
    "cf-ray",
    "nel",
    "report-to",
    "alt-svc",
    "server",
}

# Warn at import time if critical vars are absent — helps with misconfiguration
# debugging without failing hard (LiteLLM still starts, non-Subumbra models work).
if not SUBUMBRA_ACCESS_TOKEN:
    log.warning("subumbra-callback: SUBUMBRA_ACCESS_TOKEN not set — subumbra: keys will fail")
if not SUBUMBRA_HMAC_KEY:
    log.warning("subumbra-callback: SUBUMBRA_HMAC_KEY not set — subumbra: keys will fail")
if not CF_WORKER_URL:
    log.warning("subumbra-callback: CF_WORKER_URL not set — subumbra: keys will fail")

def _json_error_response(request: httpx.Request, status_code: int, message: str) -> httpx.Response:
    return httpx.Response(
        status_code,
        headers={"content-type": "application/json"},
        content=json.dumps({"error": message}).encode(),
        request=request,
    )


def _is_json_content_type(content_type: str) -> bool:
    media_type = content_type.split(";", 1)[0].strip().lower()
    return media_type == "application/json" or media_type.endswith("+json")


class _WorkerStream(httpx.AsyncByteStream):
    def __init__(self, response: httpx.Response, context_manager) -> None:
        self._response = response
        self._context_manager = context_manager
        self._closed = False

    async def __aiter__(self):
        try:
            async for chunk in self._response.aiter_raw():
                yield chunk
        finally:
            await self.aclose()

    async def aclose(self) -> None:
        if self._closed:
            return
        self._closed = True
        await self._context_manager.__aexit__(None, None, None)


class SubumbraTransport(httpx.AsyncBaseTransport):
    def __init__(self) -> None:
        self._passthrough = httpx.AsyncHTTPTransport()
        self._worker_client = httpx.AsyncClient(
            timeout=httpx.Timeout(connect=10.0, read=120.0, write=30.0, pool=30.0),
            limits=httpx.Limits(max_connections=20, max_keepalive_connections=10),
        )

    async def handle_async_request(self, request: httpx.Request) -> httpx.Response:
        ciphertext = request.headers.get("X-Subumbra-Ciphertext")
        if not ciphertext:
            return await self._passthrough.handle_async_request(request)

        content_type = request.headers.get("content-type", "")
        if content_type and not _is_json_content_type(content_type):
            return _json_error_response(request, 415, "unsupported content-type")

        body = None
        raw_body = await request.aread()
        if raw_body:
            try:
                body = json.loads(raw_body.decode("utf-8"))
            except Exception:
                return _json_error_response(request, 400, "request body must be JSON")

        clean_headers: dict[str, str] = {}
        for key, value in request.headers.items():
            key_l = key.lower()
            if key_l.startswith("x-subumbra-"):
                continue
            if key_l == "authorization":
                continue
            if key_l in {"cf-access-client-id", "cf-access-client-secret"}:
                continue
            clean_headers[key] = value

        target_url = str(request.url)
        provider = request.headers.get("X-Subumbra-Provider", "")
        proxy_payload = {
            "ciphertext": ciphertext,
            "provider": provider,
            "target_url": target_url,
            "method": request.method,
            "headers": clean_headers,
            "body": body,
            "wrapped_dek": request.headers.get("X-Subumbra-Wrapped-Dek", ""),
            "pub_key_fp": request.headers.get("X-Subumbra-Pub-Key-Fp", ""),
            "key_id": request.headers.get("X-Subumbra-Key-Id", ""),
            "enc_version": 2,
        }

        outer_headers = {
            "Content-Type": "application/json",
            "X-Subumbra-Token": SUBUMBRA_ACCESS_TOKEN,
        }
        if CF_ACCESS_CLIENT_ID:
            outer_headers["CF-Access-Client-Id"] = CF_ACCESS_CLIENT_ID
            outer_headers["CF-Access-Client-Secret"] = CF_ACCESS_CLIENT_SECRET

        log.info(
            "subumbra-transport: intercept provider=%s method=%s target_url=%s route=/proxy",
            provider,
            request.method,
            target_url,
        )

        stream_cm = self._worker_client.stream(
            "POST",
            f"{CF_WORKER_URL}/proxy",
            headers=outer_headers,
            json=proxy_payload,
        )
        worker_response = await stream_cm.__aenter__()
        if worker_response.status_code >= 400:
            log.warning(
                "subumbra-transport: worker failure provider=%s method=%s target_url=%s status=%s",
                provider,
                request.method,
                target_url,
                worker_response.status_code,
            )
        response_headers = {
            key: value
            for key, value in worker_response.headers.items()
            if key.lower() not in TRANSPORT_STRIP_HEADERS
        }
        return httpx.Response(
            worker_response.status_code,
            headers=response_headers,
            stream=_WorkerStream(worker_response, stream_cm),
            request=request,
        )


_subumbra_client: httpx.AsyncClient | None = None


def _wire_transport_once() -> None:
    """
    Wire SubumbraTransport into LiteLLM's HTTP clients.

    Called on every Subumbra request. Creates _subumbra_client once, then
    re-applies the wiring on each call so that handlers replaced by
    LiteLLM's 1-hour TTL cache eviction are always re-patched before use.
    """
    global _subumbra_client

    import litellm as _litellm
    from litellm.llms.custom_httpx.http_handler import get_async_httpx_client

    if not hasattr(_litellm, "module_level_aclient") or not hasattr(_litellm.module_level_aclient, "client"):
        raise RuntimeError(
                "Subumbra transport wiring requires litellm.module_level_aclient.client; "
            "current LiteLLM build does not expose that path"
        )

    if _subumbra_client is None:
        _subumbra_client = httpx.AsyncClient(transport=SubumbraTransport())

    _litellm.module_level_aclient.client = _subumbra_client
    _litellm.aclient_session = _subumbra_client

    for provider_name, provider, params in (
        ("anthropic",  _litellm.LlmProviders.ANTHROPIC,   None),
        ("groq",       _litellm.LlmProviders.GROQ,         {"ssl_verify": None}),
        ("deepseek",   _litellm.LlmProviders.DEEPSEEK,     {"ssl_verify": None}),
        # These providers use base_llm_http_handler → AsyncHTTPHandler (not AsyncOpenAI SDK).
        # Wire their handler.client directly so SubumbraTransport intercepts their requests.
        # params must match what BaseLLMHTTPHandler passes: {"ssl_verify": None}
        ("openrouter", _litellm.LlmProviders.OPENROUTER,   {"ssl_verify": None}),
        ("mistral",    _litellm.LlmProviders.MISTRAL,      {"ssl_verify": None}),
        ("xai",        _litellm.LlmProviders.XAI,          {"ssl_verify": None}),
    ):
        handler = get_async_httpx_client(llm_provider=provider, params=params)
        if not hasattr(handler, "client"):
            raise RuntimeError(
                f"Subumbra transport wiring requires "
                f"get_async_httpx_client({provider_name}).client"
            )
        handler.client = _subumbra_client



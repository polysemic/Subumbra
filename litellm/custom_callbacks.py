class ForgeKeyCallback(CustomLogger):
    """
    LiteLLM CustomLogger that intercepts "subumbra:" api_key values, fetches the
    V2 Subumbra envelope, and prepares the request for transport-owned /proxy
    packaging.
    """

    def __init__(self) -> None:
        super().__init__()
        # Shared async HTTP client — keeps a connection pool to subumbra-keys
        self._http = httpx.AsyncClient(
            timeout=httpx.Timeout(connect=3.0, read=10.0, write=5.0, pool=5.0),
            limits=httpx.Limits(max_connections=20, max_keepalive_connections=10),
        )

    # ── internal helpers ──────────────────────────────────────────────────────

    def _subumbra_headers(self, key_id: str) -> dict[str, str]:
        """
        Build the four headers required by subumbra-keys for a ciphertext fetch:
          X-Subumbra-Token      — bearer token
          X-Subumbra-Timestamp  — current epoch seconds
          X-Subumbra-Nonce      — single-use hex nonce
          X-Subumbra-Signature  — HMAC-SHA256(f"{key_id}:{timestamp}:{nonce}", SUBUMBRA_HMAC_KEY)

        The signature and nonce prevent replay attacks; subumbra-keys rejects
        reused requests and timestamps older than 30 s.
        """
        timestamp = str(int(time.time()))
        nonce = secrets.token_hex(16)
        sig = hmac.new(
            SUBUMBRA_HMAC_KEY,
            f"{key_id}:{timestamp}:{nonce}".encode(),
            hashlib.sha256,
        ).hexdigest()
        return {
            "X-Subumbra-Token":     SUBUMBRA_ACCESS_TOKEN,
            "X-Subumbra-Timestamp": timestamp,
            "X-Subumbra-Nonce":     nonce,
            "X-Subumbra-Signature": sig,
        }

    async def _fetch_subumbra_record(self, key_id: str) -> dict:
        """
        Fetch the V2 envelope record for key_id from subumbra-keys.

        Returns the full response dict:
          {key_id, ciphertext, provider, target_host, wrapped_dek, pub_key_fp, enc_version}

        Raises:
            httpx.HTTPStatusError  if subumbra-keys returns a non-2xx status
            httpx.RequestError     if subumbra-keys is unreachable
        """
        headers  = self._subumbra_headers(key_id)
        url      = f"{SUBUMBRA_KEYS_URL}/keys/{key_id}"

        response = await self._http.get(url, headers=headers)
        response.raise_for_status()

        return response.json()

    # ── LiteLLM hook ──────────────────────────────────────────────────────────

    async def async_pre_call_deployment_hook(
        self,
        kwargs: dict,
        call_type,
    ) -> Optional[dict]:
        """
        Called by LiteLLM AFTER a deployment is selected, BEFORE the API call
        is sent. At this point kwargs["api_key"] contains the model's api_key
        from litellm_params (e.g. "subumbra:groq_prod").

        If api_key starts with "subumbra:", injects the Subumbra envelope metadata
        needed by SubumbraTransport. The transport then packages the fully
        assembled request into the canonical /proxy envelope. All other calls
        pass through unchanged.

        Note: async_pre_call_hook fires too early (proxy request level, before
        deployment selection) and does not have access to the model's api_key.
        This hook fires at the right point in the call chain.
        """
        api_key = kwargs.get("api_key", "")
        if not isinstance(api_key, str) or not api_key.startswith("subumbra:"):
            return None

        key_id = api_key[len("subumbra:"):]
        if not key_id:
            raise ValueError("subumbra: api_key has empty key_id (expected 'subumbra:key_id')")

        # ── 1. Validate config ────────────────────────────────────────────────
        if not CF_WORKER_URL:
            raise RuntimeError(
                "CF_WORKER_URL is not set — cannot route subumbra key through CF Worker"
            )
        if not SUBUMBRA_ACCESS_TOKEN or not SUBUMBRA_HMAC_KEY:
            raise RuntimeError(
                "SUBUMBRA_ACCESS_TOKEN / SUBUMBRA_HMAC_KEY not set — run bootstrap first"
            )
        _wire_transport_once()

        # ── 2. Fetch V2 envelope record from subumbra-keys (with retry) ─────
        # HTTPStatusError (4xx/5xx) is not retried — it means the key is missing
        # or the token is wrong, and retrying will not help.
        # RequestError (network) is retried up to 3 times with exponential backoff.
        _last_net_exc: Exception | None = None
        for _attempt in range(3):
            try:
                record = await self._fetch_subumbra_record(key_id)
                break
            except httpx.HTTPStatusError as exc:
                log.error(
                    "subumbra-callback: subumbra-keys returned %s for key_id=%s",
                    exc.response.status_code, key_id,
                )
                raise RuntimeError(
                    f"subumbra-keys returned {exc.response.status_code} for key_id={key_id}"
                ) from exc
            except httpx.RequestError as exc:
                _last_net_exc = exc
                if _attempt < 2:
                    _wait = 0.1 * (2 ** _attempt)  # 0.1 s, 0.2 s
                    log.warning(
                        "subumbra-callback: subumbra-keys unreachable (attempt %d/3), "
                        "retry in %.1fs: %s",
                        _attempt + 1, _wait, exc,
                    )
                    await asyncio.sleep(_wait)
        else:
            log.error(
                "subumbra-callback: subumbra-keys unreachable after 3 attempts: %s",
                _last_net_exc,
            )
            raise RuntimeError("subumbra-keys service is unreachable") from _last_net_exc

        # Extract V2 envelope fields
        provider    = record["provider"]
        ciphertext  = record["ciphertext"]
        wrapped_dek = record.get("wrapped_dek")
        pub_key_fp  = record.get("pub_key_fp")
        enc_version = record.get("enc_version", 1)

        # Hard-reject non-V2 records (belt-and-suspenders — Worker also rejects)
        if enc_version != 2 or not wrapped_dek:
            raise RuntimeError(
                f"subumbra-keys returned a non-V2 record for key_id={key_id!r} "
                f"(enc_version={enc_version}) — re-run bootstrap to migrate"
            )

        # Use SUBUMBRA_ACCESS_TOKEN as the nominal api_key so LiteLLM sends a
        # valid (non-empty) Bearer header. The transport strips Authorization
        # before packaging the /proxy request and the Worker validates the
        # outer X-Subumbra-Token header.
        kwargs["api_key"] = SUBUMBRA_ACCESS_TOKEN

        # ── 3. Inject V2 envelope headers ─────────────────────────────────────
        if not kwargs.get("extra_headers"):
            kwargs["extra_headers"] = {}

        kwargs["extra_headers"]["X-Subumbra-Token"]       = SUBUMBRA_ACCESS_TOKEN
        kwargs["extra_headers"]["X-Subumbra-Ciphertext"]  = ciphertext
        kwargs["extra_headers"]["X-Subumbra-Provider"]    = provider
        kwargs["extra_headers"]["X-Subumbra-Key-Id"]      = key_id
        kwargs["extra_headers"]["X-Subumbra-Wrapped-Dek"] = wrapped_dek
        kwargs["extra_headers"]["X-Subumbra-Pub-Key-Fp"]  = pub_key_fp or ""

        return kwargs

    # ── optional: log outcomes for UI dashboard ───────────────────────────────

    async def async_log_success_event(self, kwargs: dict, response_obj, start_time, end_time) -> None:
        api_key = kwargs.get("api_key", "")
        if isinstance(api_key, str) and api_key == SUBUMBRA_ACCESS_TOKEN:
            model   = kwargs.get("model", "unknown")
            elapsed = (end_time - start_time).total_seconds()
            log.warning("subumbra-callback: success  model=%s  elapsed=%.2fs", model, elapsed)

    async def async_log_failure_event(self, kwargs: dict, response_obj, start_time, end_time) -> None:
        api_key = kwargs.get("api_key", "")
        if isinstance(api_key, str) and api_key == SUBUMBRA_ACCESS_TOKEN:
            model = kwargs.get("model", "unknown")
            exc   = kwargs.get("exception", "unknown error")
            log.error("subumbra-callback: failure  model=%s  error=%s", model, exc)


